console.log('**** for 예제 ****');

/* for 예제 */
for (var i=0; i<10; i++) {
    console.log(i);
}

console.log('**** while 예제 ****');
/* while 예제 */
var i = 0;
while(i<10)
{
    console.log(i);
    i++;
}

console.log('**** do-while 예제 ****');
/* do-while 예제 */
var i = 0;
do {
    console.log(i);
    i++;
} while (i<10)